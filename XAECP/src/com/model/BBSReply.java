package com.model;

public class BBSReply {

	private String subjectResponseId;
	private String subjectId;
		private String subjectTitle;
	private String responseContent;
	private String responser;
		private String responserName;
	private String responseTime;
	
	public String getSubjectResponseId() {
		return subjectResponseId;
	}
	public void setSubjectResponseId(String subjectResponseId) {
		this.subjectResponseId = subjectResponseId;
	}
	public String getSubjectId() {
		return subjectId;
	}
	public void setSubjectId(String subjectId) {
		this.subjectId = subjectId;
	}
	public String getResponseContent() {
		return responseContent;
	}
	public void setResponseContent(String responseContent) {
		this.responseContent = responseContent;
	}
	public String getResponser() {
		return responser;
	}
	public void setResponser(String responser) {
		this.responser = responser;
	}
	public String getResponserName() {
		return responserName;
	}
	public void setResponserName(String responserName) {
		this.responserName = responserName;
	}
	public String getResponseTime() {
		return responseTime;
	}
	public void setResponseTime(String responseTime) {
		this.responseTime = responseTime;
	}
	public String getSubjectTitle() {
		return subjectTitle;
	}
	public void setSubjectTitle(String subjectTitle) {
		this.subjectTitle = subjectTitle;
	}
	
	
}
